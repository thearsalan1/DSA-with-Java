package Basics;

public class ArithhemeticOperations {
    public static void main(String[] args) {
        int x=2;
        int y=8;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(x^y);
    }
}
